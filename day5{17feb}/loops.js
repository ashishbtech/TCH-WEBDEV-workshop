//print 1 to 100
for (let i = 1; i <= 100; i++) {
  console.log(i);
}


let i = 1;          // start from 1
while (i <= 100) {  // condition
  console.log(i);   // print the number
  i++;              // increment by 1
}

let j = 1;          // start from 1
do {
  console.log(j);   // print the number
  j++;              // increment by 1
} while (i <= 100); // condition checked after executing

//for table of 19
for (let i = 1; i <= 10; i++) {
  console.log("19 x " + i + " = " + 19 * i);
}


//print 100 to 0
for (let i = 100; i >= 0; i--) {
  console.log(i);
}



